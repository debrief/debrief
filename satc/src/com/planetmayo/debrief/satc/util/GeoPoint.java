package com.planetmayo.debrief.satc.util;

public class GeoPoint
{
	private double lat;
	private double lon;

	public double getLat()
	{
		return lat;
	}

	public double getLon()
	{
		return lon;
	}

	public void setLat(double lat)
	{
		this.lat = lat;
	}

	public void setLon(double lon)
	{
		this.lon = lon;
	}
}
